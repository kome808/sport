import { useState } from 'react';
import { Loader2, AlertCircle, ShieldCheck } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { useAuth } from '@/hooks/useAuth';

export default function AdminLoginPage() {
    const { signInWithGoogle } = useAuth();
    const [isLoading, setIsLoading] = useState(false);
    const [errorMessage, setErrorMessage] = useState<string | null>(null);

    const handleGoogleLogin = async () => {
        setIsLoading(true);
        setErrorMessage(null);

        // 保存導向目標
        localStorage.setItem('login_redirect_to', '/dashboard');

        // Admin login specifically requests Google auth
        const result = await signInWithGoogle();

        // Note: Actual redirection happens by Supabase auth state change listener
        // But if there's an immediate error, we handle it here
        if (!result.success && result.error) {
            setErrorMessage(result.error.message || '登入失敗，請稍後再試');
            setIsLoading(false);
        }
    };

    return (
        <div className="min-h-screen flex items-center justify-center bg-gray-100 px-4">
            <div className="w-full max-w-md">
                <div className="flex justify-center mb-8">
                    <div className="flex items-center gap-3">
                        <div className="flex h-12 w-12 items-center justify-center rounded-xl bg-blue-600 text-white font-bold text-xl shadow-lg shadow-blue-600/20">
                            TR
                        </div>
                        <span className="font-bold text-2xl text-gray-900">SportRepo</span>
                    </div>
                </div>

                <Card className="shadow-xl border-t-4 border-t-blue-600">
                    <CardHeader className="space-y-1 text-center pb-8">
                        <div className="mx-auto bg-blue-50 w-16 h-16 rounded-full flex items-center justify-center mb-4">
                            <ShieldCheck className="h-8 w-8 text-blue-600" />
                        </div>
                        <CardTitle className="text-2xl font-bold tracking-tight text-gray-900">
                            系統管理後台
                        </CardTitle>
                        <CardDescription className="text-base">
                            System Admin Portal
                        </CardDescription>
                    </CardHeader>
                    <CardContent className="space-y-6">
                        {errorMessage && (
                            <div className="flex items-center gap-2 p-3 bg-red-50 text-red-600 rounded-lg text-sm font-medium border border-red-100">
                                <AlertCircle className="h-4 w-4 flex-shrink-0" />
                                {errorMessage}
                            </div>
                        )}

                        <div className="space-y-4">
                            <div className="bg-blue-50/50 p-4 rounded-lg text-sm text-blue-800 border border-blue-100">
                                <p className="text-center font-medium">
                                    此區域僅限系統管理員存取
                                </p>
                            </div>

                            <Button
                                type="button"
                                className="w-full h-12 text-base font-medium bg-white text-gray-700 hover:bg-gray-50 border border-gray-300 shadow-sm mt-6 mb-2"
                                onClick={handleGoogleLogin}
                                disabled={isLoading}
                            >
                                {isLoading ? (
                                    <Loader2 className="h-5 w-5 animate-spin mr-2" />
                                ) : (
                                    <svg className="mr-3 h-5 w-5" viewBox="0 0 24 24">
                                        <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                                        <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                                        <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                                        <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                                    </svg>
                                )}
                                使用 Google 管理帳號登入
                            </Button>
                        </div>
                    </CardContent>
                </Card>

                <p className="text-center text-xs text-gray-400 mt-8">
                    &copy; 2026 SportRepo. All rights reserved.
                </p>
            </div>
        </div>
    );
}
